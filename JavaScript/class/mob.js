class Mob{
  constructor(x,y,width,height,image){
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
    this.image = image;
  }


  get width()
  {
    return this.width;
  }
  get height()
  {
    return this.height;
  }
  get x()
  {
    return this.x;
  }
  get y()
  {
    return this.y;
  }

  get image()
  {
    return this.image;
  }

}
